INSERT INTO center_operating_hours (
  center_id,
  day_of_week,
  open_time,
  close_time,
  is_holiday,
  is_open
)
SELECT
  c.id as center_id,
  dow.day_of_week,
  CASE
    WHEN dow.day_of_week BETWEEN 1 AND 5 THEN '09:00:00'  -- 월~금
    ELSE NULL
  END as open_time,
  CASE
    WHEN dow.day_of_week BETWEEN 1 AND 5 THEN '18:00:00'  -- 월~금
    ELSE NULL
  END as close_time,
  CASE
    WHEN dow.day_of_week IN (0, 6) THEN TRUE  -- 토일 휴무
    ELSE FALSE
  END as is_holiday,
  CASE
    WHEN dow.day_of_week BETWEEN 1 AND 5 THEN TRUE
    ELSE FALSE
  END as is_open
FROM centers c
CROSS JOIN (
  SELECT 0 AS day_of_week UNION ALL  -- 일요일
  SELECT 1 UNION ALL  -- 월요일
  SELECT 2 UNION ALL  -- 화요일
  SELECT 3 UNION ALL  -- 수요일
  SELECT 4 UNION ALL  -- 목요일
  SELECT 5 UNION ALL  -- 금요일
  SELECT 6            -- 토요일
) dow
WHERE c.id <= 8;

